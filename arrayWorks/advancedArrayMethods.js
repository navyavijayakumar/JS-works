// map()

// cube
arr=[2,3,4,5]
var cube=arr.map((num)=>num**3)
console.log(cube)

// square
var square=arr.map((num)=>num**2)
console.log(square);

// addFive
var addFive=arr.map((num)=>num+5)
console.log(addFive);

// filter()

// even
var even=arr.filter((num)=>num%2==0)
console.log(even);

// odd
var odd=arr.filter((num)=>num%2!=0)
console.log(odd);

// greaterThan 3
var numGT=arr.filter((num)=>num>3)
console.log(numGT);


